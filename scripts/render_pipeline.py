def make_cover(image_url, tmp_dir, image_b64=None):
    """Download + FFmpeg to 1080x1920 brightened JPEG. Returns path."""
    src = os.path.join(tmp_dir, 'cover_src.jpg')
    out = os.path.join(tmp_dir, 'cover.jpg')
    download_image(image_url, src, image_b64=image_b64)
    subprocess.run([
        'ffmpeg', '-y', '-i', src,
        '-vf', (
            # Stage 1d.4: match the punchier filter applied per beat in the reel.
            'scale=1080:1920:force_original_aspect_ratio=increase,'
            'crop=1080:1920,'
            'eq=contrast=1.10:saturation=1.18:gamma=0.96:brightness=0.05,'
            'unsharp=luma_msize_x=5:luma_msize_y=5:luma_amount=1.0'
        ),
        '-q:v', '3', out
    ], capture_output=True, check=True)
    return out


