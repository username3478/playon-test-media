# ── Helpers ───────────────────────────────────────────────────────────────────

def gemini_json(prompt):
    url = f'{GEMINI_API_BASE}/{GEMINI_FLASH}:generateContent?key={GEMINI_KEY}'
    payload = {
        'contents': [{'parts': [{'text': prompt}]}],
        'generationConfig': {'responseMimeType': 'application/json'},
    }
    data = json.dumps(payload).encode()
    req = urllib.request.Request(url, data=data, headers={'Content-Type': 'application/json'})
    with urllib.request.urlopen(req, timeout=30) as r:
        resp = json.loads(r.read())
    text = resp['candidates'][0]['content']['parts'][0]['text']
    return json.loads(text)


def gemini_tts(text, voice, style_prefix):
    """Call Gemini TTS, return (pcm_bytes, sample_rate)."""
    url = f'{GEMINI_API_BASE}/{GEMINI_TTS_MODEL}:generateContent?key={GEMINI_KEY}'
    payload = {
        'contents': [{'parts': [{'text': style_prefix + text}]}],
        'generationConfig': {
            'responseModalities': ['AUDIO'],
            'speechConfig': {'voiceConfig': {'prebuiltVoiceConfig': {'voiceName': voice}}},
        },
    }
    data = json.dumps(payload).encode()
    req = urllib.request.Request(url, data=data, headers={'Content-Type': 'application/json'})
    with urllib.request.urlopen(req, timeout=60) as r:
        resp = json.loads(r.read())
    part = next(p for p in resp['candidates'][0]['content']['parts'] if 'inlineData' in p)
    mime = part['inlineData']['mimeType']
    raw  = base64.b64decode(part['inlineData']['data'])
    rate = 24000
    for chunk in mime.split(';'):
        if 'rate=' in chunk:
            rate = int(chunk.split('=')[1])
    return raw, rate


def pcm_to_wav(pcm, sr, channels=1, bits=16):
    byte_rate   = sr * channels * bits // 8
    block_align = channels * bits // 8
    data_size   = len(pcm)
    header = b'RIFF' + struct.pack('<I', 36 + data_size) + b'WAVE'
    fmt    = b'fmt ' + struct.pack('<IHHIIHH', 16, 1, channels, sr,
                                   byte_rate, block_align, bits)
    data_c = b'data' + struct.pack('<I', data_size)
    return header + fmt + data_c + pcm


def audio_duration(path):
    r = subprocess.run(
        ['ffprobe', '-v', 'quiet', '-print_format', 'json', '-show_streams', path],
        capture_output=True, text=True, check=True
    )
    streams = json.loads(r.stdout)['streams']
    for s in streams:
        if s.get('codec_type') == 'audio':
            return float(s['duration'])
    raise RuntimeError(f'No audio stream in {path}')


def download_image(url, dest_path, image_b64=None):
    """Download image from URL, or decode from base64 if image_b64 provided.

    image_b64 is used as fallback when the origin blocks GH Actions runner IPs.
    """
    if image_b64:
        with open(dest_path, 'wb') as f:
            f.write(base64.b64decode(image_b64))
        return
    # Try a browser-like UA first; some WP/Cloudflare setups block curl/urllib defaults
    for ua in (
        'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/124.0 Safari/537.36',
        'Mozilla/5.0 (compatible; PuroMMA-bot/1.0)',
    ):
        req = urllib.request.Request(url, headers={'User-Agent': ua})
        try:
            with urllib.request.urlopen(req, timeout=30) as r:
                with open(dest_path, 'wb') as f:
                    f.write(r.read())
            return
        except urllib.error.HTTPError as e:
            if e.code == 403:
                continue
            raise
    raise RuntimeError(f'Image download failed with 403 for all user-agents: {url}')


def ffprobe_image_dims(path):
    r = subprocess.run(
        ['ffprobe', '-v', 'quiet', '-print_format', 'json', '-show_streams', path],
        capture_output=True, text=True, check=True
    )
    s = json.loads(r.stdout)['streams'][0]
    return int(s['width']), int(s['height'])


def sanitise_drawtext(s):
    """Escape characters that break FFmpeg drawtext filter."""
    s = s.replace('\\', '\\\\')
    s = s.replace("'", "’")   # curly apostrophe — safe in drawtext
    s = s.replace(':', '\\:')
    s = s.replace('%', '\\%')
    return s


# ── TTS pronunciation shaping ─────────────────────────────────────────────────

_ES_DIGIT_WORDS = {
    '0': 'cero', '1': 'uno', '2': 'dos', '3': 'tres', '4': 'cuatro',
    '5': 'cinco', '6': 'seis', '7': 'siete', '8': 'ocho', '9': 'nueve',
}

# Match common MMA event labels followed by an event number (e.g. "UFC 329",
# "ufc fight night 251", "PFL 7", "Bellator 305", "ONE 165").
_EVENT_NUMBER_RE = re.compile(
    r'\b(UFC(?:\s+Fight\s+Night)?|UFC\s+FN|UFC\s+on\s+ESPN|UFC\s+APEX|'
    r'PFL|Bellator|ONE|RIZIN|KSW|Cage\s+Warriors)'
    r'(\s+)(\d{2,4})\b',
    flags=re.IGNORECASE,
)


def spell_event_numbers_for_tts(text):
    """Rewrite event numbers as digit-by-digit Spanish for the TTS pass only.

    Pablo's 1d.3 verdict: Gemini TTS reads 'UFC 329' as 'UFC trescientos veintinueve'
    (cardinal). For sports event numbering Pablo wants digit-by-digit:
    'UFC tres dos nueve'. This rewrite is applied ONLY to the text fed to TTS — the
    voiceover field used for on-screen captions keeps the numerals so the caption
    reads "UFC 329" visually.

    Returns the rewritten string.
    """
    if not text:
        return text
    def repl(m):
        label, gap, num = m.group(1), m.group(2), m.group(3)
        spelled = ' '.join(_ES_DIGIT_WORDS[d] for d in num)
        return f"{label}{gap}{spelled}"
    return _EVENT_NUMBER_RE.sub(repl, text)


